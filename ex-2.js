let add = function (number_a, number_b) {
  const result = number_a + number_b;
  return result;
};

let substract = function (number_a, number_b) {
  const result = number_a - number_b;
  return result;
};

let multiply = function (number_a, number_b) {
  const result = number_a * number_b;
  return result;
};

let divide = function (number_a, number_b) {
  const result = number_a / number_b;
  return result;
};
