function add(number_a, number_b) {
  const result = number_a + number_b;
  return result;
}

function substract(number_a, number_b) {
  const result = number_a - number_b;
  return result;
}

function multiply(number_a, number_b) {
  const result = number_a * number_b;
  return result;
}

function divide(number_a, number_b) {
  const result = number_a / number_b;
  return result;
}
