let add = (number_a, number_b) => {
  const result = number_a + number_b;
  return result;
};

let substract = (number_a, number_b) => {
  const result = number_a - number_b;
  return result;
};

let multiply = (number_a, number_b) => {
  const result = number_a * number_b;
  return result;
};

let divide = (number_a, number_b) => {
  const result = number_a / number_b;
  return result;
};
